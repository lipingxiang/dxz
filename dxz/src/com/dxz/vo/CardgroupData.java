package com.dxz.vo;

public class CardgroupData {
	
	private int payid;
	
	private int roomCard;

	private int giftCard;

	private int money;
	
	
	public int getPayid() {
		return payid;
	}

	public void setPayid(int payid) {
		this.payid = payid;
	}

	public int getRoomCard() {
		return roomCard;
	}

	public void setRoomCard(int roomCard) {
		this.roomCard = roomCard;
	}

	public int getGiftCard() {
		return giftCard;
	}

	public void setGiftCard(int giftCard) {
		this.giftCard = giftCard;
	}

	public int getMoney() {
		return money;
	}

	public void setMoney(int money) {
		this.money = money;
	}

}
